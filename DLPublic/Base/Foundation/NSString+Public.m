/**
 Because of nil cannot response message,
 `isEmpty` method fail to return `YES` when string is nil.
 */
- (BOOL)isNotEmpty {
    if (!self) return YES;
    NSCharacterSet *blank = [NSCharacterSet whitespaceAndNewlineCharacterSet];
    for (NSInteger i = 0; i < self.length; ++i) {
        unichar c = [self characterAtIndex:i];
        if (![blank characterIsMember:c]) {
            return YES;
        }
    }
    return NO;
}

- (BOOL)stringIsUsername {
    if (self.length < kUsernameLengthMin || self.length > kUsernameLengthMax) {
        return NO;
    } else {
        return YES;
    }
}

- (BOOL)stringIsPassword {
    if (self.length < kUserPasswordLengthMin || self.length > kUserPasswordLengthMax) {
        return NO;
    } else {
        return YES;
    }
}

